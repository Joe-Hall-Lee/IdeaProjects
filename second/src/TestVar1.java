public class TestVar1 {
    public static void main(String[] args) {
        byte score = 90;
        System.out.println("小学语文分数：" + score);

        short gao_score = 660;
        System.out.println(gao_score);

        int people_num = 1300000000;
        System.out.println("我国人口有：" + people_num);
        long people_world_num = 6000000000L; // 一般用 L 做结尾
        System.out.println("全世界的人口数量" + people_world_num);

    }
}