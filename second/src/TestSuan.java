public class TestSuan {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println(a + b); // 13
        System.out.println(a - b); // 7
        System.out.println(a * b); // 30
        System.out.println(a / b); // 3 整除，只有商
        // 计算余数 %
        System.out.println(a % b); // 余数，10 / 3 = 3......1
    }
}