        // == 条件判断，判断左右两端的是否一致

        System.out.println(1000 == 1000); // true
        System.out.println(1000 == 999); // false

        // != 判断左右两端是否不一致

        System.out.println(999 != 1000); // true
        System.out.println(1000 != 1000); // false
